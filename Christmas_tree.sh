read -p "please enter a number: " N

for i in $(seq 1 $N)
do
        for x in $(seq 1 $(($N-$i)))
        do
                echo -n " "
        done
        for y in $(seq 1 $(($i*2-1)))
        do
                echo -n "*"
        done
        echo
done

